namespace csharp_day_1.models
{
  public class Card
  {
    public User To { get; set; }
    public User From { get; set; }
    private string Message { get; set; }

    public string GetCard()
    {
      return $@"
{Message}
   ***********                  ***********
  *****************            *****************
*********************        *********************
***********************      ***********************
************************    ************************
*************************  *************************
 **************************************************
  ************************************************
    ********************************************
      ****************************************
         **********************************
           ******************************
              ************************
                ********************
                   **************
                     **********
                       ******
                         **
            TO: {To.Name} | FROM: {From.Name}
      ";
    }


    public Card(User to, User from, string message)
    {
      To = to;
      From = from;
      Message = message;
    }
  }
}